public class Operaciones {


    public Operaciones(){

    }


    //Metodos
    public int sumar( int a, int b){
        return a + b;



    }

    public int sumar(int a, int b, int c, int d){
        return a + b + c + d;
    }

    public double sumar( double a, double b){
        return a + b;

    }

}
